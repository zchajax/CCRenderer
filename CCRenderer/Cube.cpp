Cube* Cube::Create(const char* imagePath)
{
	Cube* cube = new Cube();

	if (cube)
	{
		HRESULT hr = cube->Init(imagePath);

        if (FAILED(hr))
        {
            delete cube;
            return nullptr;
        }
	}

	return cube;
}

